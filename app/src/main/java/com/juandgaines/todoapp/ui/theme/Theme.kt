//TODO: 3.2 Cambiar nombre de tema
@Composable
fun TodoAppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    // TODO: 3.4 Remover dynamicColor
    content: @Composable() () -> Unit
) {
    // TODO: 3.4 Remover dynamicColor
    val colorScheme = if (darkTheme)
        darkScheme
    else
        lightScheme

  MaterialTheme(
    colorScheme = colorScheme,
    typography = AppTypography,
    content = content
  )
}

